def parse_parity_check_matrix(matrix_str: str):
    hx_rows = []
    hz_rows = []
    
    # Split the input string into lines
    lines = matrix_str.strip().split('\n')
    
    for line in lines:
        line = line.strip().strip('[]')
        # Split each line into HX and HZ parts
        hx_str, hz_str = line.split('|')
        # Convert string elements to integers and store in lists
        hx_row = [int(bit) for bit in hx_str.strip().split()]
        hz_row = [int(bit) for bit in hz_str.strip().split()]
        hx_rows.append(hx_row)
        hz_rows.append(hz_row)
    
    # Convert lists to numpy arrays
    hx = np.array(hx_rows, dtype=int)
    hz = np.array(hz_rows, dtype=int)
    
    return hx, hz

def real_greedy_parallel_schedule(x_arr, z_arr):
    schedule = []
    x_sets = [set(x) for x in x_arr]
    z_sets = [set(z) for z in z_arr]

    for i in range(len(x_arr)):
        placed = False
        for batch in schedule:
            valid = True
            for j in batch:
                # Direct conflicts
                x_conflict = not x_sets[i].isdisjoint(x_sets[j])
                z_conflict = not z_sets[i].isdisjoint(z_sets[j])

                # Cross-type conflicts
                xz_conflict = not x_sets[i].isdisjoint(z_sets[j])
                zx_conflict = not z_sets[i].isdisjoint(x_sets[j])

                # If more than one type of conflict occurs, it's invalid
                conflict_types = sum([
                    x_conflict,
                    z_conflict,
                    xz_conflict,
                    zx_conflict
                ])

                if conflict_types > 1:
                    valid = False
                    break
            if valid:
                batch.append(i)
                placed = True
                break
        if not placed:
            schedule.append([i])

    total_time = sum(
        max(len(x_arr[i]) + len(z_arr[i]) for i in batch) for batch in schedule
    )

    return schedule, total_time

